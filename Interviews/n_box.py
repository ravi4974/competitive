from single_box import Rect
def find_rects(m):
	rects=[]
	for i,row in enumerate(m):
		try:
			j=0
			while j<len(row):
				j=row.index(0,j)
				r=Rect(top=i,left=j)
				r.trace(m)
				rects.append(r)
				j=r.right+1
		except:
			pass
	return rects

